package com.everydots.permission.component.entity.main;


import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.everydots.permission.component.entity.IdEntity;

@Entity
@Table(name="user_detail")
public class UserDetail extends IdEntity{
	
    
	/**
	 * 用户ID
	 */
    @Column(nullable=false, length=20)
    private String userid;
    
	/**
	 * 用户名
	 */
    @Column(length=20)
    private String username;
    
	/**
	 * 证件类型，见字典 personCertType
	 */
    @Column(length=2)
    private String certtype;
    
	/**
	 * 证件号码
	 */
    @Column(length=30)
    private String certno;
    
	/**
	 * 性别，见字典 sex
	 */
    @Column(length=2)
    private String sex;
    
	/**
	 * 生日
	 */
    @Column(length=10)
    private String birthday;
    
	/**
	 * 电话号码，手机
	 */
    @Column(length=20)
    private String phoneno;
    
	/**
	 * 联系电话，座机
	 */
    @Column(length=20)
    private String telphoneno;
    
	/**
	 * email
	 */
    @Column(length=50)
    private String email;
    
	/**
	 * 地址
	 */
    @Column(length=100)
    private String address;
    
	/**
	 * 备注
	 */
    @Column(length=100)
    private String remark;
    
    
	
	/**
	 * @param userid the userid to set
	 */
    public void setUserid(String userid){
       this.userid = userid;
    }
    
    /**
     * @return the userid 
     */
    public String getUserid(){
       return this.userid;
    }
	
	/**
	 * @param username the username to set
	 */
    public void setUsername(String username){
       this.username = username;
    }
    
    /**
     * @return the username 
     */
    public String getUsername(){
       return this.username;
    }
	
	/**
	 * @param certtype the certtype to set
	 */
    public void setCerttype(String certtype){
       this.certtype = certtype;
    }
    
    /**
     * @return the certtype 
     */
    public String getCerttype(){
       return this.certtype;
    }
	
	/**
	 * @param certno the certno to set
	 */
    public void setCertno(String certno){
       this.certno = certno;
    }
    
    /**
     * @return the certno 
     */
    public String getCertno(){
       return this.certno;
    }
	
	/**
	 * @param sex the sex to set
	 */
    public void setSex(String sex){
       this.sex = sex;
    }
    
    /**
     * @return the sex 
     */
    public String getSex(){
       return this.sex;
    }
	
	/**
	 * @param birthday the birthday to set
	 */
    public void setBirthday(String birthday){
       this.birthday = birthday;
    }
    
    /**
     * @return the birthday 
     */
    public String getBirthday(){
       return this.birthday;
    }
	
	/**
	 * @param phoneno the phoneno to set
	 */
    public void setPhoneno(String phoneno){
       this.phoneno = phoneno;
    }
    
    /**
     * @return the phoneno 
     */
    public String getPhoneno(){
       return this.phoneno;
    }
	
	/**
	 * @param telphoneno the telphoneno to set
	 */
    public void setTelphoneno(String telphoneno){
       this.telphoneno = telphoneno;
    }
    
    /**
     * @return the telphoneno 
     */
    public String getTelphoneno(){
       return this.telphoneno;
    }
	
	/**
	 * @param email the email to set
	 */
    public void setEmail(String email){
       this.email = email;
    }
    
    /**
     * @return the email 
     */
    public String getEmail(){
       return this.email;
    }
	
	/**
	 * @param address the address to set
	 */
    public void setAddress(String address){
       this.address = address;
    }
    
    /**
     * @return the address 
     */
    public String getAddress(){
       return this.address;
    }
	
	/**
	 * @param remark the remark to set
	 */
    public void setRemark(String remark){
       this.remark = remark;
    }
    
    /**
     * @return the remark 
     */
    public String getRemark(){
       return this.remark;
    }
}
